# Criação da lista contendo 10 nomes
nomes = ['William', 'Banks', 'Dias', 'João', 'Filipe', 'Daniel', 'Karython', 'Gomes', 'Kauê', 'Henrique']

# Impressão apenas do primeiro elemento
print(nomes[0])

# Impressão apenas do quinto elemento
print(nomes[4])

# Impressão do segundo e terceiro elementos da lista
print(nomes[1:3])

# Impressão dos 6 primeiros elementos da lista
print(nomes[:6])

# Impressão dos 3 últimos elementos da lista
print(nomes[7:])